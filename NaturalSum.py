#WAP to find sum of first n natural numbers.

print("FINDING THE SUM OF FIRST N NATURAL NUMBERS")
n = int(input("Enter the value of n:"))    #taking input from the user
sum = n*(n+1)/2                           #formula to find the sum of first n natural numbers
print("The sum of first",n,"natural numbers is:",sum)  #printing the sum of first n natural numbers
