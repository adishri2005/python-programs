#WAP where the radius is taken as input to compute the area of a circle.

print("FINDING THE AREA OF A CIRCLE")
r = float(input("Enter the radius of the circle:"))                 #taking input from the user
area = 3.14*r*r                                                   #computing the area of the circle
print("The area of the circle is:",area)                           #printing the area of the circle



#In this program, we have taken the radius of the circle as input from the user.
#The area of the circle is calculated using the formula 3.14*r*r.
#The area of the circle is then printed.